/**
 * content 디렉토리를 스캔하여 src/content/index.ts를 자동 생성하는 스크립트.
 *
 * 파일명 규칙: <번호>. <id>.<md|tsx>
 * - md  → { type: 'markdown', content: string }
 * - tsx → { type: 'component', component: ComponentType }
 * - home, settings → 특수 탭 (CONTENT_TAB_DEFS에서 제외)
 * - md 파일의 첫 번째 `# heading`이 탭 라벨로 사용됨
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CONTENT_DIR = join(__dirname, '..', 'src', 'content');
const GENERATED_DIR = join(__dirname, '..', 'src', 'generated');
const OUTPUT_FILE = join(GENERATED_DIR, 'content.ts');

const SPECIAL_TABS = new Set(['home', 'settings']);
const FILE_PATTERN = /^(\d+)\.\s+(.+)\.(md|tsx)$/;

// content 디렉토리 스캔 및 파싱
const files = readdirSync(CONTENT_DIR)
  .filter((f) => FILE_PATTERN.test(f))
  .map((filename) => {
    const match = filename.match(FILE_PATTERN);
    const [, numStr, id, ext] = match;
    return { filename, order: parseInt(numStr, 10), id, ext };
  })
  .sort((a, b) => a.order - b.order);

// md 파일에서 첫 번째 # heading 추출
function extractHeading(filepath) {
  const content = readFileSync(filepath, 'utf-8');
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1].trim() : null;
}

// import 문 생성
const imports = files.map(({ filename, id, ext }) => {
  const importPath = `../content/${filename.replace(/\\/g, '/')}`;
  if (ext === 'tsx') {
    // PascalCase로 변환
    const varName =
      id.charAt(0).toUpperCase() + id.slice(1) + 'Content';
    return `import ${varName} from '${importPath.replace(/\.tsx$/, '')}';`;
  } else {
    const varName = id + 'Md';
    return `import ${varName} from '${importPath}';`;
  }
});

// CONTENT_TAB_DEFS 생성 (특수 탭 제외)
const tabDefs = files
  .filter(({ id }) => !SPECIAL_TABS.has(id))
  .map(({ id, ext }) => {
    let label = id;
    if (ext === 'md') {
      const filepath = join(CONTENT_DIR, files.find((f) => f.id === id).filename);
      const heading = extractHeading(filepath);
      if (heading) label = heading;
    }
    return `  { id: '${id}', label: '${label}' },`;
  });

// TAB_CONTENT 생성
const tabContentEntries = files.map(({ id, ext }) => {
  if (ext === 'tsx') {
    const varName =
      id.charAt(0).toUpperCase() + id.slice(1) + 'Content';
    return `  ${id}: { type: 'component', component: ${varName} },`;
  } else {
    const varName = id + 'Md';
    return `  ${id}: { type: 'markdown', content: ${varName} },`;
  }
});

// index.ts 조립
const output = `// This file is auto-generated by scripts/generate-content-index.mjs
// Do not edit manually.

import type { ComponentType } from 'react';

${imports.join('\n')}

export type TabContent =
  | { type: 'markdown'; content: string }
  | { type: 'component'; component: ComponentType<{ accentColor: string }> };

export const CONTENT_TAB_DEFS: { id: string; label: string }[] = [
${tabDefs.join('\n')}
];

export const TAB_CONTENT: Record<string, TabContent> = {
${tabContentEntries.join('\n')}
};
`;

mkdirSync(GENERATED_DIR, { recursive: true });
writeFileSync(OUTPUT_FILE, output, 'utf-8');
console.log(`Generated ${OUTPUT_FILE}`);
console.log(`  Tabs: ${files.map((f) => f.id).join(', ')}`);
